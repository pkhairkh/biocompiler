# ─── Helper: create app with specific env vars ──────────────────────

def _make_app_with_env(env_vars: dict[str, str], tmp_key_file: Path | None = None):
    """Create a FastAPI app with specific environment variable overrides.

    We patch os.environ before importing/reloading the api module to
    control the module-level auth configuration.
    """
    import importlib
    from biocompiler import api as api_module

    # Build a complete env dict
    env = dict(os.environ)
    # Remove any existing biocompiler auth vars
    for key in list(env.keys()):
        if key.startswith("BIOCOMPILER_API") or key == "BIOCOMPILER_AUTH_MODE":
            del env[key]
    # Apply test overrides
    env.update(env_vars)

    # Patch _get_api_key_file_path if a tmp_key_file is provided
    patches = [patch.dict(os.environ, env, clear=False)]

    if tmp_key_file is not None:
        patches.append(
            patch.object(api_module, "_get_api_key_file_path", return_value=tmp_key_file)
        )

    # We need to reload the module for the module-level code to re-execute
    # with the new env vars. But that's complex; instead, we directly
    # manipulate the module-level variables.
    return patches, api_module


class TestAutoGeneratedAPIKey:
    """Test that a random API key is generated when BIOCOMPILER_API_KEY is not set."""

    def test_auto_key_generated_when_no_env_var(self, tmp_path):
        """When BIOCOMPILER_API_KEY is not set, an auto-generated key should be created."""
        from biocompiler.api import _generate_and_persist_api_key, _get_api_key_file_path

        key_file = tmp_path / ".biocompiler" / "api_key"

        with patch("biocompiler.api._get_api_key_file_path", return_value=key_file):
            with patch.dict(os.environ, {}, clear=False):
                # Remove the env var if it exists
                os.environ.pop("BIOCOMPILER_API_KEY", None)
                key = _generate_and_persist_api_key()

        assert key is not None
        assert len(key) == 64  # 32 bytes = 64 hex chars
        assert key_file.exists()
        assert key_file.read_text().strip() == key

    def test_auto_key_persisted_and_reused(self, tmp_path):
        """The auto-generated key should be reused on subsequent startups."""
        from biocompiler.api import _generate_and_persist_api_key

        key_file = tmp_path / ".biocompiler" / "api_key"

        with patch("biocompiler.api._get_api_key_file_path", return_value=key_file):
            key1 = _generate_and_persist_api_key()
            key2 = _generate_and_persist_api_key()

        assert key1 == key2

    def test_auto_key_file_permissions(self, tmp_path):
        """The persisted key file should have restrictive permissions (0o600)."""
        from biocompiler.api import _generate_and_persist_api_key

        key_file = tmp_path / ".biocompiler" / "api_key"

        with patch("biocompiler.api._get_api_key_file_path", return_value=key_file):
            _generate_and_persist_api_key()

        # On some systems chmod may not work (e.g. Windows), so just check the file exists
        assert key_file.exists()
        # On Unix, check permissions
        if os.name == "posix":
            stat_info = key_file.stat()
            mode = stat_info.st_mode & 0o777
            assert mode == 0o600

    def test_auto_key_is_hex_string(self, tmp_path):
        """The auto-generated key should be a valid hex string."""
        from biocompiler.api import _generate_and_persist_api_key

        key_file = tmp_path / ".biocompiler" / "api_key"

        with patch("biocompiler.api._get_api_key_file_path", return_value=key_file):
            key = _generate_and_persist_api_key()

        # Should be valid hex
        int(key, 16)  # Raises ValueError if not hex


