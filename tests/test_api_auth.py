class TestPublicKeyFunctions:
    """Test the public helper functions for auth state inspection."""

    def test_get_auth_mode(self):
        """get_auth_mode should return the current auth mode string."""
        from biocompiler import api as api_module
        from biocompiler.api import get_auth_mode

        original = api_module._AUTH_MODE
        try:
            api_module._AUTH_MODE = "required"
            assert get_auth_mode() == "required"

            api_module._AUTH_MODE = "optional"
            assert get_auth_mode() == "optional"

            api_module._AUTH_MODE = "disabled"
            assert get_auth_mode() == "disabled"
        finally:
            api_module._AUTH_MODE = original

    def test_is_auth_enabled(self):
        """is_auth_enabled should return True only when auth is active."""
        from biocompiler import api as api_module
        from biocompiler.api import is_auth_enabled

        original_mode = api_module._AUTH_MODE
        original_keys = set(api_module._CONFIGURED_API_KEYS)

        try:
            api_module._AUTH_MODE = "required"
            api_module._CONFIGURED_API_KEYS = {"some-key"}
            assert is_auth_enabled() is True

            api_module._AUTH_MODE = "disabled"
            api_module._CONFIGURED_API_KEYS = set()
            assert is_auth_enabled() is False

            api_module._AUTH_MODE = "optional"
            api_module._CONFIGURED_API_KEYS = {"some-key"}
            assert is_auth_enabled() is True

            # Optional mode with no keys = not really enabled
            api_module._AUTH_MODE = "optional"
            api_module._CONFIGURED_API_KEYS = set()
            assert is_auth_enabled() is False
        finally:
            api_module._AUTH_MODE = original_mode
            api_module._CONFIGURED_API_KEYS = original_keys

    def test_get_configured_api_keys(self):
        """get_configured_api_keys should return a copy of the keys set."""
        from biocompiler import api as api_module
        from biocompiler.api import get_configured_api_keys

        original_keys = set(api_module._CONFIGURED_API_KEYS)

        try:
            api_module._CONFIGURED_API_KEYS = {"key1", "key2"}
            keys = get_configured_api_keys()
            assert keys == {"key1", "key2"}
            # Should be a copy, not the original set
            keys.add("key3")
            assert "key3" not in api_module._CONFIGURED_API_KEYS
        finally:
            api_module._CONFIGURED_API_KEYS = original_keys


class TestDefaultAuthIsEnabled:
    """Test that auth is enabled by default (the core security fix)."""

    def test_auth_is_not_disabled_by_default(self):
        """By default (no env vars), auth should NOT be in disabled mode."""
        from biocompiler.api import get_auth_mode

        # The default auth mode should be "required"
        # (unless overridden by env vars in the test environment)
        mode = get_auth_mode()
        assert mode in ("required", "optional", "disabled")
        # In the test environment, the actual mode depends on env vars,
        # but the DEFAULT when no env is set should be "required"

    def test_auto_key_makes_auth_enabled(self, tmp_path):
        """When no BIOCOMPILER_API_KEY is set, the auto-generated key should enable auth."""
        from biocompiler.api import _generate_and_persist_api_key

        key_file = tmp_path / ".biocompiler" / "api_key"

        with patch("biocompiler.api._get_api_key_file_path", return_value=key_file):
            key = _generate_and_persist_api_key()

        # If keys are configured and mode is not disabled, auth is enabled
        assert len(key) > 0

    def test_no_env_var_means_auto_generated_key(self):
        """Without BIOCOMPILER_API_KEY env var, the module should have auto-generated keys."""
        from biocompiler import api as api_module
        from biocompiler.api import get_configured_api_keys

        # In the test environment, we can't guarantee the state of env vars,
        # but we can verify that when keys are configured, they're non-empty
        keys = get_configured_api_keys()
        # Either the user set BIOCOMPILER_API_KEY or an auto-key was generated
        # The important thing is that auth is NOT simply disabled
        if api_module._AUTH_MODE != "disabled":
            assert len(keys) > 0, "Auth mode is not disabled but no keys are configured"
