"""
Tests for BioCompiler API authentication — default auth-on behavior.

Covers:
1. Auto-generated API key when BIOCOMPILER_API_KEY is not set
2. API key persistence to ~/.biocompiler/api_key
3. BIOCOMPILER_AUTH_MODE env var: "required" (default), "optional", "disabled"
4. HTTP 401 for unauthenticated requests in "required" mode
5. X-Auth-Warning response header in "optional" mode
6. BIOCOMPILER_API_KEY=disabled explicitly disables auth (with warning)
7. --no-auth CLI flag disables auth (with warning)
8. Health endpoint auth_enabled field reflects actual auth state
9. Valid API key grants access in all non-disabled modes
"""

